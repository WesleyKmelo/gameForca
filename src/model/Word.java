package model;

public class Word {
    String word;
    int sense;
    int wid;


    public String getWord() {
        return word;
    }
    public void setWord(String word) {
        this.word = word;
    }
    public int getSense() {
        return sense;
    }
    public void setSense(int sense) {
        this.sense = sense;
    }
    public int getWid() {
        return wid;
    }
    public void setWid(int wid) {
        this.wid = wid;
    }
}


